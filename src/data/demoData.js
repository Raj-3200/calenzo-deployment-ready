export const demoQueue = demoAppointments
  .filter((appointment) => appointment.appointment_date === today)
  .map((appointment, index) => {
    const waitBase = Math.max(0, index - 1) * 8
    return {
      id: `queue-${appointment.id}`,
      clinic_id: DEFAULT_CLINIC_ID,
      appointment_id: appointment.id,
      patient_id: appointment.patient_id,
      token_number: appointment.token_number,
      queue_date: appointment.appointment_date,
      current_status:
        appointment.status === 'confirmed' ? 'waiting' :
        appointment.status === 'completed' ? 'completed' :
        appointment.status === 'in_progress' ? 'in_progress' :
        appointment.status === 'arrived' ? 'arrived' :
        appointment.status === 'cancelled' ? 'cancelled' : 'waiting',
      estimated_wait_time: waitBase + 15,
      delay_minutes: 15,
      position: index + 1,
      updated_at: new Date().toISOString(),
    }
  })

