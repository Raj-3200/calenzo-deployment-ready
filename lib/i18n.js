export function languageOrDefault(language) {
  return LANGUAGE_OPTIONS.some((option) => option.code === language) ? language : DEFAULT_LANGUAGE
}

export function sectionCopy(section, language) {
  const normalized = languageOrDefault(language)
  return section[normalized] || section[DEFAULT_LANGUAGE]
}

export function serviceDisplay(service, language) {
  const normalized = languageOrDefault(language)
  const translation = SERVICE_TRANSLATIONS[normalized]?.[service?.title]

  return {
    title: translation?.[0] || service?.title || '',
    description: translation?.[1] || service?.description || '',
  }
}
