export function getService(serviceId) {
  return demoServices.find((service) => service.id === serviceId)
}

export function getPatient(patientId) {
  return demoPatients.find((patient) => patient.id === patientId)
}

export function hydrateAppointment(appointment) {
  return {
    ...appointment,
    patient: getPatient(appointment.patient_id),
    service: getService(appointment.service_id),
  }
}

export const hydratedAppointments = demoAppointments.map(hydrateAppointment)

export const hydratedQueue = demoQueue.map((item) => {
  const appointment = hydrateAppointment(demoAppointments.find((entry) => entry.id === item.appointment_id))
  return {
    ...item,
    appointment,
    patient: appointment.patient,
    service: appointment.service,
  }
})

export const hydratedFollowUps = demoFollowUps.map((followUp) => ({
  ...followUp,
  patient: getPatient(followUp.patient_id),
  appointment: hydrateAppointment(demoAppointments.find((entry) => entry.id === followUp.appointment_id) || demoAppointments[0]),
}))
